package com.ams.bean;

import java.util.Date;

public class ShiftTime {
	
	private Date maxIn;
	private Date minOut;
	/**
	 * @return the maxIn
	 */
	public Date getMaxIn() {
		return maxIn;
	}
	/**
	 * @param maxIn the maxIn to set
	 */
	public void setMaxIn(Date maxIn) {
		this.maxIn = maxIn;
	}
	/**
	 * @return the minOut
	 */
	public Date getMinOut() {
		return minOut;
	}
	/**
	 * @param minOut the minOut to set
	 */
	public void setMinOut(Date minOut) {
		this.minOut = minOut;
	}
	
	

}
